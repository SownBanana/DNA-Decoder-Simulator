def str2ncs(st):
    ncs = ""
    for c in st.upper():
        assert c in c2n_dict, c +' not have nucleotide encode'
        if c in c2n_dict:
                ncs += c2n_dict[c]
        else:
            ncs += '_'
    return ncs

def ncs2str(ncs):
    ncs = textwrap.wrap(ncs.upper(), 3)
    st = ""
    for nc in ncs:
        if nc in n2c_dict:
            st += n2c_dict[nc]
        else:
            st += '_'
    return st